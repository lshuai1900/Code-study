#include<iostream>
using namespace std;

//引用的本质在C++内部实现是一个指针常量


//内部发现是引用，转换为：int* const ref = &a;
void func(int& ref)
{
	ref = 100;//ref是引用，自动帮我们转换为：*ref = 20;
}


int main5()
{
	int a = 10;

	int& ref = a;
	ref = 20;//内部发现ref是引用，自动帮我们转换为：*ref = 20;

	cout << "a=" << a << endl;
	cout << "ref=" << ref << endl;

	func(a);



	return 0;
}